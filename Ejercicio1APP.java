public class Ejercicio1APP {
	public static void main(String[] args) {

		int num1 = 8, num2 = 3; // Aquí declaro las variables con valor ya establecido.

		System.out.println(num1 + " + " + num2 + "= " + (num1 + num2)); // Esta línea mostrará la suma
		System.out.println(num1 + " - " + num2 + "= " + (num1 - num2)); // Esta línea mostrará la resta
		System.out.println(num1 + " * " + num2 + "= " + (num1 * num2)); // Esta línea mostrará la multiplicación
		System.out.println(num1 + " / " + num2 + "= " + (num1 / num2)); // Esta línea mostrará la división
		System.out.println(num1 + " % " + num2 + "= " + (num1 % num2)); // Esta línea mostrará el módulo (resto de la división)
	}
}
